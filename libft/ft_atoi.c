#include "libft.h"

int	ft_atoi(const char *nptr)
{
	char	*str;
	int		res;
	int		hyphen;
	int		i;

	str = (char *) nptr;
	res = 0;
	hyphen = 1;
	i = 0;
	while (str[i] != '0' && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			hyphen = -1;
		i++;
	}
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	return (res * hyphen);
}
