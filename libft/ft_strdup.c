#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*ptr;
	int		i;
	size_t	s_len;

	s_len = ft_strlen((char *) s);
	ptr = malloc(sizeof(char) * (s_len + 1));
	if (ptr == NULL)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
